pub fn find_keyword(keyword_str: &String) -> Keyword {
    // 转化为大写
    let upper_keyword_str = keyword_str.to_uppercase();

    match upper_keyword_str.as_str() {
        "AND" => Keyword::And,
        "AS" => Keyword::As,
        "ASC" => Keyword::Asc,
        "BEGIN" => Keyword::Begin,
        "BOOL" => Keyword::Bool,
        "BOOLEAN" => Keyword::Boolean,
        "BY" => Keyword::By,
        "CHAR" => Keyword::Char,
        "COMMIT" => Keyword::Commit,
        "CREATE" => Keyword::Create,
        "CROSS" => Keyword::Cross,
        "DEFAULT" => Keyword::Default,
        "DELETE" => Keyword::Delete,
        "DESC" => Keyword::Desc,
        "DOUBLE" => Keyword::Double,
        "DROP" => Keyword::Drop,
        "EXPLAIN" => Keyword::Explain,
        "FALSE" => Keyword::False,
        "FLOAT" => Keyword::Float,
        "FROM" => Keyword::From,
        "GROUP" => Keyword::Group,
        "HAVING" => Keyword::Having,
        "INDEX" => Keyword::Index,
        "INFINITY" => Keyword::Infinity,
        "INNER" => Keyword::Inner,
        "INTEGER" => Keyword::Integer,
        "INSERT" => Keyword::Insert,
        "INT" => Keyword::Int,
        "INTO" => Keyword::Into,
        "IS" => Keyword::Is,
        "JOIN" => Keyword::Join,
        "KEY" => Keyword::Key,
        "LEFT" => Keyword::Left,
        "LIKE" => Keyword::Like,
        "LIMIT" => Keyword::Limit,
        "NAN" => Keyword::NaN,
        "NOT" => Keyword::Not,
        "NULL" => Keyword::Null,
        "OF" => Keyword::Of,
        "OFFSET" => Keyword::Offset,
        "ON" => Keyword::On,
        "ONLY" => Keyword::Only,
        "OR" => Keyword::Or,
        "ORDER" => Keyword::Order,
        "OUTER" => Keyword::Outer,
        "PRIMARY" => Keyword::Primary,
        "READ" => Keyword::Read,
        "REFERENCES" => Keyword::References,
        "RIGHT" => Keyword::Right,
        "ROLLBACK" => Keyword::Rollback,
        "SELECT" => Keyword::Select,
        "STRING" => Keyword::String,
        "SET" => Keyword::Set,
        "SYSTEM" => Keyword::System,
        "TABLE" => Keyword::Table,
        "TEXT" => Keyword::Text,
        "TIME" => Keyword::Time,
        "TRANSACTION" => Keyword::Transaction,
        "TRUE" => Keyword::True,
        "UNIQUE" => Keyword::Unique,
        "UPDATE" => Keyword::Update,
        "VALUES" => Keyword::Values,
        "VARCHAR" => Keyword::VarChar,
        "WHERE" => Keyword::Where,
        "WRITE" => Keyword::Write,
        _ => Keyword::UserIdent,
    }
}
